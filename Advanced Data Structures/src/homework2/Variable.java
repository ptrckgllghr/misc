package homework2;

public class Variable {

	private String var;
	private int value;
	
	
	
	public Variable(String var, int value) 
	{
		this.var = var;
		this.value = value;
	}
	/**
	 * @return the var
	 */
	public String getVar() {
		return var;
	}

	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}

	
	
	
	
}
